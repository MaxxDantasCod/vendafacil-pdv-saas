            <p>O VendaFácil ("nós") é uma plataforma de gestão de vendas. Somos controladores dos seus dados pessoais nos termos da Lei Geral de Proteção de Dados (Lei 13.709/2018).</p>

            <h2 class="text-xl font-semibold text-white">2. Dados que coletamos</h2>
            <ul class="list-disc pl-6">
                <li><strong>Cadastro:</strong> nome, e-mail, senha criptografada</li>
                <li><strong>Faturamento:</strong> CPF ou CNPJ (coletado apenas no momento do upgrade para plano pago)</li>
