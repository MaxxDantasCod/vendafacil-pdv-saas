            <h2 class="text-xl font-semibold text-white">3. Por que coletamos CPF/CNPJ</h2>
            <p>Coletamos seu CPF ou CNPJ exclusivamente para emissão de nota fiscal e cumprimento de obrigações tributárias, através do nosso processador de pagamentos <strong>Asaas</strong>. Sem este dado não é possível gerar boletos, Pix ou faturas válidas.</p>

            <h2 class="text-xl font-semibold text-white">4. Como protegemos</h2>
            <p>Seu CPF/CNPJ é armazenado com <strong>criptografia AES-256</strong> no banco de dados e nunca é exibido por completo na plataforma. Utilizamos HTTPS, controle de acesso e o Asaas é certificado PCI-DSS. Não compartilhamos seus dados com terceiros para marketing.</p>

            <h2 class="text-xl font-semibold text-white">5. Compartilhamento</h2>
            <p>Compartilhamos apenas com: (a) Asaas Gestão Financeira (processamento de pagamentos); (b) autoridades legais quando exigido por lei.</p>

            <h2 class="text-xl font-semibold text-white">6. Seus direitos (LGPD)</h2>
            <p>Você pode a qualquer momento: acessar, corrigir, anonimizar ou solicitar a exclusão dos seus dados através do menu "Minha Conta" ou pelo e-mail privacidade@vendafacil.com.br. A exclusão do CPF impossibilitará a manutenção de assinaturas ativas.</p>

            <h2 class="text-xl font-semibold text-white">7. Retenção</h2>
            <p>Mantemos seus dados enquanto sua conta estiver ativa e por até 5 anos após o cancelamento para fins fiscais, conforme exigência legal.</p>

